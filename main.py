background = pygame.image.load(path.join(img_folder, 'fon.png'))
background_rect = background.get_rect()
player_img = pygame.image.load(path.join(img_folder, 'w-01-150.png'))
enemy_img = pygame.image.load(path.join(img_folder, 'Ghost.png'))

all_sprites = pygame.sprite.Group()
enemy = pygame.sprite.Group()
bullets = pygame.sprite.Group()
player = Player()
all_sprites.add(player)
for i in range(8):
    en = Enemy()
    all_sprites.add(en)
    enemy.add(en)
score = 0

running = True
while running:
    clock.tick(FPS)
    for event in pygame.event.get():
        if event.type == pygame.QUIT:
            running = False
        elif event.type == pygame.KEYDOWN:
            if event.key == pygame.K_SPACE:
                player.gun()

    all_sprites.update()

    hits = pygame.sprite.groupcollide(enemy, bullets, True, True)
    for hit in hits:
        score += 10
        en = Enemy()
        all_sprites.add(en)
        enemy.add(en)

    hits = pygame.sprite.spritecollide(player, enemy, False, pygame.sprite.collide_circle)
    if hits:
        running = False

    screen.fill(BLACK)
    screen.blit(background, background_rect)
    all_sprites.draw(screen)
    draw_text(screen, str(score), 18, WIDTH / 2, 10 )
    pygame.display.update()


pygame.quit()